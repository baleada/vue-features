<template>
  <span ref="stub"></span>
  <button @click="() => (color = 'blue')">set color</button>
</template>

<script lang="ts">
import { defineComponent, ref } from 'vue'
import { bindStyle } from '../../../../../../src/extracted/bindStyle'

export default defineComponent({
  setup () {
    const stub = ref(null),
          color = ref('red')

    bindStyle({
      elementOrElements: stub,
      property: 'backgroundColor',
      value: color,
      watchSources: [],
    })

    return { stub, color }
  }
})
</script>
